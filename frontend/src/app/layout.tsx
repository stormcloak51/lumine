import type { Metadata } from 'next'
import './globals.css'

import '@mantine/core/styles.css'

import { ColorSchemeScript, createTheme, MantineColorsTuple, MantineProvider } from '@mantine/core'
import * as font from '../fonts/fonts'
import { ThemeProvider } from 'next-themes'
import ClientProvider from './ClientProvider'

export const metadata: Metadata = {
  title: 'lumine',
  description: 'Generated by create next app',
}

const myColor: MantineColorsTuple = [
	'#ffd37d',
	'#fff8e1',
	'#ffefcb',
	'#ffdd9a',
	'#ffcb64',
	'#ffbb38',
	'#ffb11b',
	'#ffac09',
	'#e39600',
	'#ca8500',
	'#b07200',
]

const theme = createTheme({
	colors: {
		myColor,
	},
})

export default async function RootLayout({
	children,
}: Readonly<{
	children: React.ReactNode
}>) {
	return (
		<html lang='en'>
			<head>
				{/* <meta name='apple-mobile-web-app-title' content='lumine' /> */}
				<ColorSchemeScript />
			</head>
			<body
				className={`${font.geistSans.variable} ${font.geistMono.variable} ${font.helvetica_bold.variable} ${font.helvetica_regular.variable} antialiased`}>
				<ClientProvider>
					<ThemeProvider defaultTheme='dark'>
						<MantineProvider defaultColorScheme='dark' theme={theme}>
							{children}
						</MantineProvider>
					</ThemeProvider>
				</ClientProvider>
			</body>
		</html>
	)
}
